//-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
//  Convert signed number (integer = 4 Bytes) to character array
//
//    num: signed integer
//    en_neg: enable negative numbers
//    digits: number of digits. If num is higher then number of digits, ++++ is replaced
//    in string. If num is lower then number of digits -1 minus sign is replaced in result.
//-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
void bl_Number_To_Indexed_Array(int num, unsigned int en_neg, unsigned int digits)
{
	int i;
	int zero_suppress = 1;
	int pos = 0;
	int minus = (num < 0) && (en_neg != 0);
	int divisor = 1;
	unsigned char over;
	int digit_nr; // to store original number of digits

	if (digits > 9) digits = 9;
	digit_nr = digits;

	if (minus)
	{
		num = -num;
		pos = 1;
		digits--;
	}
	while (digits-- > 1) divisor = divisor * 10;
	i = num / divisor;
	if (i > 9) // number is too high to display in given number of positions
	{
		if (minus) over = '-'; else over = '+';
		pos = 0;
		while (digit_nr--) blNumberToDispArr[pos++] = over;
		divisor = 0;
	}

	while (divisor > 0)
	{
			i = num / divisor;
			num = num - i*divisor;
			if ((i != 0) || (!zero_suppress) || (divisor == 1))
			{
					if (minus)
					{
						blNumberToDispArr[pos-1] = '-';
							minus = 0;
					}
					blNumberToDispArr[pos] = 0x30 + i;
					zero_suppress = 0;
			}
			else blNumberToDispArr[pos] = ' ';
			pos++;
			divisor = divisor / 10;
	}
	blNumberToDispArr[pos] = 0;
}

//-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
//  Show Character (5x7)
//
//    b: Ascii
//    x: Start X Address
//    y: Start Y Address
//	  invert: Inversion of the character
//-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
void bl_Show_Font57(unsigned char b, unsigned char x, unsigned char y, unsigned char invert)
{
unsigned char const *Src_Pointer;
unsigned char i,Font,MSB,LSB, bn;
unsigned char fontM = bl_FontBrightness << 4;

	Src_Pointer=&bl_Ascii_0[b][0];

	bl_Set_Remap_Format(0x54);
	for(i=0;i<=4;i+=2)
	{
		LSB=*Src_Pointer;
		Src_Pointer++;
		if(i == 4)
		{
			MSB=0x00;
		}
		else
		{
			MSB=*Src_Pointer;
			Src_Pointer++;
		}
		if (invert)
		{
			MSB = ~MSB;
			LSB = ~LSB;
		}
 		bl_Set_Column_Address(x,x);
		bl_Set_Row_Address(y,y+7);

		for (bn=0;bn<8;bn++)
		{
				Font = 0;
				if (MSB&0x01) Font = fontM;
				if (LSB&0x01) Font |= bl_FontBrightness;
				bl_Write_Data(Font);
				MSB >>= 1;
				LSB >>= 1;
		}
		x++;
	}
	bl_Set_Remap_Format(0x50);
}

//-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
//  Show String
//
//    posx: Start X Address
//    posy: Start Y Address
//    * Must write "0" in the end...
//-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=-=
void bl_Show_String(const char *Data_Pointer, unsigned char posx, unsigned char posy, int positions)
{
	while(*Data_Pointer && positions--)
	{
		bl_Show_Font57(*Data_Pointer++, posx, posy, 0);
		posx += 3;
	}
}

