void bl_DisplayInit(void)				// VCC Supplied Externally
{
	RES_1;
	RES_1;
	RES_1;
	bl_Write_Command(0xAE);				// Display Off (0x00/0x01)
	bl_Write_2_Cmnds(0xB3, 0x91);		// Set Clock as 135 Frames/Sec
	bl_Write_2_Cmnds(0xA8, 63);			// 1/64 Duty (0x0F~0x5F)
	bl_Write_2_Cmnds(0xA2, 76);			// Shift Mapping RAM Counter (0x00~0x5F)
	bl_Write_2_Cmnds(0xA1, 0);			// Set Mapping RAM Display Start Line (0x00~0x5F)
	bl_Write_2_Cmnds(0xAD, 0x02|0);		// Disable Embedded DC/DC Converter (0x00/0x01)
	bl_Write_2_Cmnds(0xA0, 0x50);		// Set Column Address 0 Mapped to SEG0
										//     Disable Nibble Remap
										//     Horizontal Address Increment
										//     Scan from COM[N-1] to COM0
										//     Enable COM Split Odd Even
	bl_Write_Command(0x84|0x02);		// Set Full Current Range
	//---- set Gray Scale Table
	bl_Write_Command(0xB8);				// Set Gray Scale Table
	bl_Write_Command(0x01);				//   Gray Scale Level 1
	bl_Write_Command(0x11);				//   Gray Scale Level 3 & 2
	bl_Write_Command(0x22);				//   Gray Scale Level 5 & 4
	bl_Write_Command(0x32);				//   Gray Scale Level 7 & 6
	bl_Write_Command(0x43);				//   Gray Scale Level 9 & 8
	bl_Write_Command(0x54);				//   Gray Scale Level 11 & 10
	bl_Write_Command(0x65);				//   Gray Scale Level 13 & 12
	bl_Write_Command(0x76);				//   Gray Scale Level 15 & 14
	//---- end of Gray Scale Table
	bl_Write_2_Cmnds(0x81, 0x7F);		// Set Scale Factor of Segment Output Current Control
	bl_Write_2_Cmnds(0xB2, 0x46);		// Set Frame Frequency
	bl_Write_2_Cmnds(0xB1, 0x22);		// Set Phase 1 as 2 Clocks & Phase 2 as 2 Clocks
	bl_Write_2_Cmnds(0xBC, 0x10);		// Set Pre-Charge Voltage Level
	bl_Write_2_Cmnds(0xB4, 0x07);		// Set Pre-Charge Compensation
	bl_Write_2_Cmnds(0xB0, 0x08|0x20);
	bl_Write_2_Cmnds(0xBE, 0x02);		// Set High Voltage Level of COM Pin
	bl_Write_2_Cmnds(0xBF, 0x02|0x0C);	// Set Low Voltage Level of SEG Pin
	bl_Write_Command(0xA4);				// Normal Display Mode (0x00/0x01/0x02/0x03)

	bl_Clear_Screen();

	bl_Write_Command(0xAE|0x01);		// Display On (0x00/0x01)

	blScreenSaverCtc = BL_SCREEN_SAVER_CTC_RELOAD;
	blScreenSaverMod = 1000;
	blScreenSaverRow = 0;
	blScreenSaverCol = 0;
	bl_FontBrightness = 0x0f;
	RES_1;
	RES_1;
	_bl_bootloader_background();
}

void bl_DisplayReset(void)
{
	RES_0;
}

