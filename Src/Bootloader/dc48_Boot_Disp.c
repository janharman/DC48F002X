void _bl_refresh_display_item(int idx)
{
	const tBlDispItem * di = &blDispItem[idx];
	switch (di->type)
	{
	case 0: // small integer number
//			bl_Write_SmallNumber_To_Pos(*di->pVal, di->y, di->x, di->w);
			break;
	case 1: // Normal integer number
			bl_Write_Number_To_Pos(*di->pVal, di->x, di->y, di->w);
			break;
	}
}

void bl_DisplayRefresh(void)
{
	TIM7->SR = 0;
	if (blScreenSaverCtc)
	{
		if (blCommActive)
		{
			blCommActive = 0;
			blScreenSaverCtc = BL_SCREEN_SAVER_CTC_RELOAD;
		}
		blScreenSaverCtc--;
		blScreenSaverMod = 1000;
		for (int i=0; i<BL_DISP_ITEMS_CNT; i++)
			if (dispItemMirrorVal[i] != *blDispItem[i].pVal)
			{
				dispItemMirrorVal[i] = *blDispItem[i].pVal;
				_bl_refresh_display_item(i);
			}
	}
	else	// Screen-Saver display
	{
		if (blScreenSaverMod == 1000)	// clear display
		{
			bl_Clear_Screen();
			unsigned char r = blCircularCounter & 0x07;
			unsigned char c = blCircularCounter % 44;
			bl_Show_String("DC48-BOOTLOADER", r, c, 16);		// sirka Small String je 6px
		}
		blScreenSaverMod--;
		if (!blScreenSaverMod) blScreenSaverMod = 1000;
		if (blCommActive)
		{
			blCommActive = 0;
			blScreenSaverCtc = BL_SCREEN_SAVER_CTC_RELOAD;
			bl_Clear_Screen();
			_bl_bootloader_background();
		}
	}

	// LED DMX -> RDM present
	if (blRdmPresentLED)
	{
		blRdmPresentLED--;
		LED_DMX_ON;
	}
	else LED_DMX_OFF;
}

